#include "libft.h"

int	ft_cmp_nb(char *a, char *b)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (a[i] == '+')
		i++;
	if (b[j] == '+')
		j++;
	while (a[i] || b[j])
	{
		if (a[i] == b[j])
		{
			i++;
			j++;
		}
		else
			return (1);
	}
	return (0);
}
